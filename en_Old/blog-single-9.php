<h1>Asia Pacific Excellence Award</h1>
<h2>CMD Karun Kaura awarded with Asia Pacific Excellence Award at Kathmandu, Nepal</h2>
</header>
<p>The International Conference on 'Indo-Nepal Friendship and Economic Cooperation' was jointly organized by Citizens Integration Peace Institute and Nepal India Forum for Peace and Development. It is aimed at fostering and strengthening ties of friendship and economic brotherhood between the two countries.</p>
<p>Held in the capital city of Kathmandu, the event saw many distinguished personalities making their presence felt from the fields of business and politics. To support the spirit of economic development and prosperity, a total of 20 entrepreneurs were awarded by Citizen Integration Peace Institute- a Nepal-India forum for peace and development.</p>
<p>Adding a remarkable feat to their glorifying career, Mr. Karun Kaura and Mr. Mitin Pattnaik , Directors Kaya Blenders and Distillers Ltd were awarded with the Asia Pacific Excellence Award For outstanding individual achievements and distinguished services to the nation on 16th June, 2017. The award was presented by Rt. Hon'ble Shri Ram Baran Yadav, former President of Federal Democratic Republic of Nepal who was the chairperson for the occasion along with Prime Minister and Minister for Foreign affairs of Nepal Krishna Bahadur Mahara, Ambassador of India to Nepal Manjeet Singh Puri.</p>
<p>Kaya Blenders and Distillers Ltd, founded by Mr. Karun Kaura in Patiala, is an emerging group of liquor manufacturers and retailers that are creating their own niche in the liquor industry and spreading consumer base in national markets as well as overseas. The directors of the company give much emphasis to consistent, research, innovation and quality-testing. Before making his foray in the liquor industry, Mr.Kaura worked as Media Advisor to Former Chief Minister Punjab.</p>
<p>The Asia Pacific Excellence Award highlights the outstanding milestones that Kaya Blenders and Distillers have achieved. The award is given to upcoming new businessmen and entrepreneurs for their staggering business acumen and contribution in upholding the ideals of mutual trust and cooperation between the two nations by economic endeavors. Both Mr. Kaura and Mr. Pattnaik believe in working towards their business with all their heart and this is the driving force that inspires them to achieve new heights for the organization. Kaya Blenders and Distillers Ltd. will soon have a strong foothold in Nepal and is planning to increase its market share in the Asia-Pacific region.</p>
<p>Among other notable guests, Deputy Prime Minister and Minister for Foreign affairs of Nepal Krishna Bahadur Mahara, Ambassador of India to Nepal </p>
<p>Manjeet Singh Puri and other delegates and government officials attended the event.</p>


